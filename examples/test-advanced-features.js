#!/usr/bin/env node

const { AppAtOnceClient } = require('@appatonce/node-sdk');

async function testAdvancedFeatures() {
  try {
    console.log('🚀 Testing advanced SDK features...');
    
    // Test against cloud API for advanced features
    const client = new AppAtOnceClient(process.env.APPATONCE_API_KEY);
    
    console.log('\n📊 Testing server connection...');
    try {
      const health = await client.ping();
      console.log('✅ Cloud server health:', health.status);
    } catch (healthError) {
      console.log('⚠️ Cloud server connection failed:', healthError.message);
      console.log('   Falling back to localhost...');
      
      // Note: The SDK now automatically uses the correct endpoint
      // based on internal configuration (localhost for dev, api.appatonce.com for production)
      const localClient = new AppAtOnceClient(process.env.APPATONCE_API_KEY);
      
      const localHealth = await localClient.ping();
      console.log('✅ Local server health:', localHealth.status);
      // Switch to local client for rest of tests
      Object.assign(client, localClient);
    }
    
    // Test AI functionality
    console.log('\n🤖 Testing AI functionality...');
    try {
      const aiResponse = await client.ai.chat({
        messages: [
          { role: 'user', content: 'Hello, this is a test from the AppAtOnce SDK' }
        ],
        model: 'gpt-3.5-turbo'
      });
      console.log('✅ AI chat works:', aiResponse.choices[0]?.message?.content?.substring(0, 50) + '...');
    } catch (aiError) {
      console.log('⚠️ AI test result:', aiError.message);
    }
    
    // Test PDF generation
    console.log('\n📄 Testing PDF generation...');
    try {
      const pdfResult = await client.pdf.generateFromHTML(
        '<html><body><h1>Test PDF</h1><p>Generated by AppAtOnce SDK</p></body></html>',
        {
          format: 'A4',
          orientation: 'portrait'
        }
      );
      console.log('✅ PDF generation works:', pdfResult.url ? 'URL generated' : 'No URL');
    } catch (pdfError) {
      console.log('⚠️ PDF test result:', pdfError.message);
    }
    
    // Test OCR functionality
    console.log('\n👁️ Testing OCR functionality...');
    try {
      const ocrResult = await client.ocr.extractFromImage(
        'https://via.placeholder.com/400x200/000000/FFFFFF?text=Test+OCR+Text',
        {
          languages: ['en'],
          mode: 'accurate'
        }
      );
      console.log('✅ OCR works:', ocrResult.text ? 'Text extracted' : 'No text');
    } catch (ocrError) {
      console.log('⚠️ OCR test result:', ocrError.message);
    }
    
    // Test image processing
    console.log('\n🖼️ Testing image processing...');
    try {
      const imageResult = await client.imageProcessing.processImage(
        'https://via.placeholder.com/800x600/FF0000/FFFFFF?text=Test+Image',
        {
          resize: { width: 400, height: 300, fit: 'cover' },
          format: 'webp',
          quality: 85
        }
      );
      console.log('✅ Image processing works:', imageResult.url ? 'URL generated' : 'No URL');
    } catch (imageError) {
      console.log('⚠️ Image processing test result:', imageError.message);
    }
    
    // Test storage functionality
    console.log('\n💾 Testing storage functionality...');
    try {
      const storageResult = await client.storage.uploadFromUrl(
        'https://via.placeholder.com/200x200/00FF00/FFFFFF?text=Storage+Test',
        {
          filename: 'test-storage.png',
          folder: 'sdk-tests',
          public: true
        }
      );
      console.log('✅ Storage upload works:', storageResult.url ? 'File uploaded' : 'No URL');
    } catch (storageError) {
      console.log('⚠️ Storage test result:', storageError.message);
    }
    
    // Test push notifications
    console.log('\n📱 Testing push notifications...');
    try {
      const pushResult = await client.push.send({
        to: 'test-device-token',
        title: 'SDK Test Notification',
        body: 'This is a test from AppAtOnce SDK',
        data: { test: true }
      });
      console.log('✅ Push notification works:', pushResult.id ? 'Notification sent' : 'No ID');
    } catch (pushError) {
      console.log('⚠️ Push notification test result:', pushError.message);
    }
    
    // Test payment functionality
    console.log('\n💳 Testing payment functionality...');
    try {
      const paymentResult = await client.payment.createPaymentMethod({
        type: 'card',
        details: {
          number: '4242424242424242', // Stripe test card
          exp_month: 12,
          exp_year: 2025,
          cvc: '123'
        }
      });
      console.log('✅ Payment method creation works:', paymentResult.id ? 'Method created' : 'No ID');
    } catch (paymentError) {
      console.log('⚠️ Payment test result:', paymentError.message);
    }
    
    // Test workflow functionality
    console.log('\n⚙️ Testing workflow functionality...');
    try {
      const workflowResult = await client.workflow.createWorkflow({
        name: 'SDK Test Workflow',
        description: 'Test workflow created by SDK',
        steps: [
          {
            id: 'start',
            name: 'Start',
            type: 'function',
            config: { function: 'test' }
          }
        ],
        trigger: {
          type: 'manual'
        }
      });
      console.log('✅ Workflow creation works:', workflowResult.id ? 'Workflow created' : 'No ID');
    } catch (workflowError) {
      console.log('⚠️ Workflow test result:', workflowError.message);
    }
    
    console.log('\n🎉 Advanced SDK test completed!');
    console.log('\n📋 Summary:');
    console.log('   - Most features may require server-side setup or cloud deployment');
    console.log('   - Advanced features like AI, OCR, PDF may need external services configured');
    console.log('   - Check server logs for more details on failed tests');
    
  } catch (error) {
    console.error('\n❌ Main error:', error.message);
    if (error.response) {
      console.error('Response status:', error.response.status);
      console.error('Response data:', error.response.data);
    }
  }
}

// Check API key
if (!process.env.APPATONCE_API_KEY) {
  console.error('❌ APPATONCE_API_KEY environment variable is required');
  process.exit(1);
}

testAdvancedFeatures().catch(console.error);